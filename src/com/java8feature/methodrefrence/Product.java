package com.java8feature.methodrefrence;

class Product {
	private int rollNo;
	private String name;

	public Product(int rollNo, String name) {
		this.rollNo = rollNo;
		this.name = name;
	}

	public int getRollNo() {
		return rollNo;
	}

	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Product [rollNo=" + rollNo + ", name=" + name + "]";
	}
}